# 2. How atoms create light
# ==========================================================
print(f"""
  ┌──────────────────────────────────────────────────────────────┐
  │  КАК АТОМ СОЗДАЁТ СВЕТ                                      │
  └──────────────────────────────────────────────────────────────┘

  Атом = вихрь (электрон) вокруг клубка вихрей (ядро).

  Электрон на орбите = вихрь, обращающийся вокруг центра.
  Это ВРАЩАЮЩИЙСЯ ДИПОЛЬ — как антенна.

  Когда электрон перескакивает с орбиты на орбиту:
    ΔE = E₂ − E₁ = hν

  Вихрь ДЁРГАЕТСЯ → рябь в среде → поперечная волна → СВЕТ.

  Аналогия:
    Палец в воде вибрирует → волны на поверхности.
    Вихрь в эфире вибрирует → EM-волна в среде.

  Частота волны = частота вибрации вихря:
    ν = ΔE/h

  ПОЧЕМУ ДИСКРЕТНЫЕ ЧАСТОТЫ (спектр)?
  Потому что орбиты квантованы (как стоячие волны на струне).
  Вихрь может быть только в определённых устойчивых положениях.
  Переход между ними → дискретная частота → спектральная линия.
""")



# 4. Light vs Sound — key difference
# ==========================================================
print(f"""
  ┌──────────────────────────────────────────────────────────────┐
  │  СВЕТ vs ЗВУК — ОДНА СРЕДА ИЛИ ДВЕ?                         │
  └──────────────────────────────────────────────────────────────┘

  ЗВУК в воздухе:
    Среда: воздух (ρ = 1.2 кг/м³)
    Тип: продольная (сжатие-расширение)
    Скорость: 343 м/с
    Источник: вибрация твёрдого тела → давит на воздух

  СВЕТ в эфире:
    Среда: эфир (ρ ≈ μ₀ = 1.26×10⁻⁶)
    Тип: ПОПЕРЕЧНАЯ (сдвиг, не сжатие!)
    Скорость: {c:.3e} м/с
    Источник: вибрация вихря (электрона) → колеблет эфир

  КЛЮЧЕВОЕ ОТЛИЧИЕ:
    Звук — ПРОДОЛЬНАЯ волна в воздухе.
    Свет — ПОПЕРЕЧНАЯ волна в эфире.
    Эфир не сжимается → нет продольных EM-волн.
    (Кроме нейтрино — это продольная волна, но другой механизм.)

  ПОЧЕМУ СВЕТ ПОПЕРЕЧНЫЙ?
    Эфир = сверхтекучая + сверхпроводящая среда.
    Жёсткий на СДВИГ (G = 1.13×10¹¹ Па ≈ сталь).
    НЕСЖИМАЕМЫЙ (K → ∞).
    → Продольные волны подавлены (c_longitudinal → ∞).
    → Поперечные проходят со скоростью c = √(G/ρ).

  Как стеклянный стержень:
    Продольный удар → звук проходит быстро.
    Поперечный удар → волна проходит медленнее.
    Но эфир НАОБОРОТ: поперечные = конечная c, продольные → ∞.
    Потому что K/G → ∞ (идеальная несжимаемость).
""")



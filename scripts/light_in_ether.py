print(f"""
  ┌──────────────────────────────────────────────────────────────┐
  │  ФОТОН — ЧАСТИЦА ИЛИ ВОЛНА?                                  │
  └──────────────────────────────────────────────────────────────┘

  В эфирной модели: фотон — это ВОЛНОВОЙ ПАКЕТ.

  Не бесконечная синусоида (Максвелл).
  Не точечная частица (Эйнштейн).
  А: локализованная порция ряби с энергией E = hν.

  Почему E = hν?
  Потому что вихрь (электрон) прыгает между дискретными
  состояниями. Каждый прыжок = один пакет волн.
  Энергия пакета = разница энергий орбит = hν.

  Почему "частица" в фотоэффекте?
  Волновой пакет передаёт энергию ЦЕЛИКОМ одному электрону.
  Как цунами: одна волна — один удар. Не размазано по берегу.

  Почему "волна" в интерференции?
  Потому что это ВОЛНА в среде. Две волны складываются.
  Как рябь на воде проходит через две щели → интерференция.

  Дуализм "частица-волна" = артефакт ЗАБЫТОЙ СРЕДЫ.
  Если помнить что свет = рябь в эфире — дуализма нет.
  Есть просто волновой пакет определённой энергии.

  Аналогия:
    Бросил камень в воду → круги (волны).
    Бросил ОДИН камень → ОДИН набор кругов.
    Каждый набор несёт определённую энергию.
    Это "фотон воды" — волновой пакет от одного удара.
    Нет дуализма — есть просто волна.
""")

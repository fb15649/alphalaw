print(f"""
  ┌──────────────────────────────────────────────────────────────┐
  │  ГОРЕНИЕ И НАКАЛ — КАК ОГОНЬ СОЗДАЁТ СВЕТ                   │
  └──────────────────────────────────────────────────────────────┘

  ГОРЕНИЕ (свеча):
    Химическая реакция → выделяется энергия → атомы ускоряются.
    Быстрые атомы = горячий газ (T ~ 1000-2000 K).
    Горячий атом = вихрь, который колотится о соседей.
    Каждый удар → электрон возбуждается → перескок → фотон.
    → Непрерывный спектр (хаотические удары, много частот).

  НИТЬ НАКАЛА (лампочка):
    Ток → электроны текут → нагревают нить (T ~ 2700 K).
    Горячая нить: атомы колеблются → электроны колеблются.
    → Спектр Планка (тепловое излучение).

  ЛАЗЕР:
    Все атомы возбуждены ОДИНАКОВО → все прыгают ОДНОВРЕМЕННО.
    → Одна частота, когерентная волна (стоячая волна в резонаторе).

  В эфирной модели:
    Нагрев = больше кинетической энергии вихрей.
    Больше энергия → больше амплитуда колебаний.
    Больше амплитуда → ярче рябь в эфире → ярче свет.
    Выше T → чаще колебания → выше частота → голубее свет.

  Закон Вина: λ_max × T = 2.898×10⁻³ м·К
""")

T_candle = 1800
T_bulb = 2700
T_sun = 5778

for name, T in [("Свеча", T_candle), ("Лампочка", T_bulb), ("Солнце", T_sun)]:
    lam = 2.898e-3 / T
    nu = c / lam
    E = h * nu / eV
    print(f"  {name:12s}: T = {T:5d} K → λ_max = {lam*1e9:.0f} нм, ν = {nu:.2e} Гц, E = {E:.2f} эВ")

print(f"""
  Каждый: горячий вихрь вибрирует → рябь в эфире → фотон.
  T определяет частоту → частота определяет цвет.
""")


